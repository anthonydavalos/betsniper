export const getAllPinnaclePrematchOdds = async () => {
    const mapFromFeed = (events = []) => {
        const oddsMap = new Map();

        for (const ev of events) {
            const homePart = ev.participants?.find(p => p.alignment === 'home') || { name: ev.home || 'Unknown Home' };
            const awayPart = ev.participants?.find(p => p.alignment === 'away') || { name: ev.away || 'Unknown Away' };

            if (isExcludedMarketVariant({
                home: homePart.name,
                away: awayPart.name,
                league: ev.league?.name,
                units: ev.units
            })) {
                continue;
            }

            let moneyline = null;
            const totals = [];

            if (ev.markets && Array.isArray(ev.markets)) {
                const mlMarket = ev.markets.find(m => m.type === 'moneyline' && m.period === 0 && m.status === 'open');
                if (mlMarket?.prices) {
                    const p = {};
                    mlMarket.prices.forEach(pr => {
                        const val = Math.abs(pr.price) > 20 ? americanToDecimal(pr.price) : pr.price;
                        p[pr.designation] = Number(val.toFixed(3));
                    });
                    moneyline = {
                        home: p.home,
                        away: p.away,
                        draw: p.draw || 0,
                        isLive: false
                    };
                }

                ev.markets.filter(m => m.type === 'total' && m.period === 0 && m.status === 'open').forEach(m => {
                    const overP = m.prices?.find(x => x.designation === 'over');
                    const underP = m.prices?.find(x => x.designation === 'under');
                    if (overP && underP) {
                        const valO = Math.abs(overP.price) > 20 ? americanToDecimal(overP.price) : overP.price;
                        const valU = Math.abs(underP.price) > 20 ? americanToDecimal(underP.price) : underP.price;
                        totals.push({
                            line: overP.points || m.points,
                            over: Number(valO.toFixed(3)),
                            under: Number(valU.toFixed(3))
                        });
                    }
                });
            }

            oddsMap.set(ev.id, {
                id: ev.id,
                match: `${homePart.name} vs ${awayPart.name}`,
                home: homePart.name,
                away: awayPart.name,
                score: '0-0',
                time: 'PRE',
                date: ev.startTime || ev.date || new Date().toISOString(),
                league: ev.league?.name || 'Unknown League',
                isLive: false,
                moneyline,
                doubleChance: moneyline ? calculateDCFromMoneyline(moneyline.home, moneyline.draw || 100, moneyline.away) : null,
                totals
            });
        }

        return oddsMap;
    };

    // A) Cache local prematch (preferido)
    if (fs.existsSync(LOCAL_PREMATCH_DATA_PATH)) {
        try {
            const fileContent = fs.readFileSync(LOCAL_PREMATCH_DATA_PATH, 'utf-8');
            const feed = JSON.parse(fileContent);
            const fromLocal = mapFromFeed(feed.events || []);
            if (fromLocal.size > 0) return fromLocal;
        } catch (e) {
            console.warn("⚠️ Error leyendo Feed Local PREMATCH. Fallback a API.", e.message);
        }
    }

    // B) Fallback API (solo si el cache falta o está vacío)
    try {
        console.log('   🌐 [Pinnacle] Fallback PREMATCH via API (cache miss).');
        const [marketData, matchupData] = await Promise.all([
            pinnacleClient.get('/sports/29/markets/straight', { primaryOnly: false, withSpecials: false, _: Date.now() }),
            pinnacleClient.get('/sports/29/matchups', { brandId: 0, _: Date.now() })
        ]);

        const { nowUtcMs, startUtcMs, endUtcMs } = getPrematchWindowUtc();
        const metaMap = new Map();

        (matchupData || []).forEach(m => {
            const startTs = m.startTime ? new Date(m.startTime).getTime() : 0;
            const isLikelyLive = !!m.liveMode || m.state?.minutes !== undefined;

            if (!startTs || startTs < startUtcMs || startTs > endUtcMs) return;
            if (isLikelyLive) return;

            const home = m.participants?.find(p => p.alignment === 'home');
            const away = m.participants?.find(p => p.alignment === 'away');

            if (isExcludedMarketVariant({
                home: home?.name,
                away: away?.name,
                league: m.league?.name,
                units: m.units
            })) return;

            metaMap.set(m.id, {
                id: m.id,
                participants: m.participants,
                home: home?.name || 'Home',
                away: away?.name || 'Away',
                startTime: m.startTime,
                league: m.league,
                units: m.units
            });
        });

        const grouped = new Map();
        (marketData || []).forEach(m => {
            if (!metaMap.has(m.matchupId)) return;
            if (m.period !== 0 || m.status !== 'open') return;

            if (!grouped.has(m.matchupId)) grouped.set(m.matchupId, []);
            grouped.get(m.matchupId).push(m);
        });

        const events = [];
        grouped.forEach((markets, matchupId) => {
            const meta = metaMap.get(matchupId);
            events.push({
                id: matchupId,
                participants: meta.participants,
                startTime: meta.startTime,
                league: meta.league,
                units: meta.units,
                markets
            });
        });

        return mapFromFeed(events);
    } catch (e) {
        console.error('❌ Error fallback API PREMATCH:', e.message);
        return new Map();
    }
};

/**
 * Obtiene las cuotas en vivo de un partido específico desde Pinnacle Arcadia.
 * @param {string|number} pinnacleMatchId - ID del partido en Pinnacle.
 * @returns {Object|null} Objeto con propiedad { home, draw, away, timestamp } o null.
 */
export const getPinnacleLiveOdds = async (pinnacleMatchId) => {
    if (!pinnacleMatchId) return null;
    
    try {
        // Usamos el endpoint que devuelve mercados "straight" (1x2, OU, HC)
        // Usamos pinnacleClient para manejo de identidad y anti-delay
        const data = await pinnacleClient.get(`/matchups/${pinnacleMatchId}/markets/related/straight`);

        // DEBUG: Imprimir raw data para debuggear live status
        // console.log("RAW PINNACLE DATA SAMPLE:", JSON.stringify(data.slice(0, 2), null, 2));


        // ----------------------------------------------------------------
        // 1. MONEYLINE (1x2) & DOUBLE CHANCE
        // ----------------------------------------------------------------
        // Buscamos periodo 0 (Match) o periodo 1/2 si es live (pero para "Full Match 1x2" sigue siendo periodo 0)
        // [Security Fix] Filtramos múltiples mercados para evitar "Rest of Match"
        // El verdadero mercado 1x2 es el que tiene el cutoffAt más antiguo (original).
        
        const allMoneylines = data.filter(m => (m.key === 's;0;m' || m.type === 'moneyline') && m.period === 0 && m.status === 'open');
        
        // Ordenar: Más antiguo primero
        allMoneylines.sort((a, b) => (a.cutoffAt || '9999').localeCompare(b.cutoffAt || '9999'));

        const mlMarket = allMoneylines[0];
        
        // DEBUG: Si hay duplicados, avisar
        if (allMoneylines.length > 1) {
             console.log(`🛡️  Filtered ${allMoneylines.length} ML markets. Selected Cutoff: ${mlMarket.cutoffAt}`);
        }
        
        // --- LIVE CHECK ---
        // Verificamos si hay mercados de periodo 1 o 2, lo cual confirma que es LIVE
        const isLiveConfirmed = data.some(m => m.period === 1 || m.period === 2);

        
        let moneyline = null;
        let doubleChance = null;

        if (mlMarket && mlMarket.prices) {
            const prices = {};
            mlMarket.prices.forEach(p => {
                const decimal = americanToDecimal(p.price);
                if (decimal && p.designation) prices[p.designation.toLowerCase()] = Number(decimal.toFixed(3));
            });

            if (prices.home && prices.away) {
                const draw = prices.draw || 0;
                moneyline = {
                    home: prices.home,
                    away: prices.away,
                    draw: draw,
                    isLive: isLiveConfirmed
                };

                // Calcular Double Chance Real (Fair Probabilities derived from ML)
                // Primero obtenemos probs sin vig del ML
                const invH = 1 / prices.home;
                const invA = 1 / prices.away;
                const invD = draw > 0 ? 1 / draw : 0;
                const totalInv = invH + invA + invD;

                const probH = invH / totalInv;
                const probA = invA / totalInv;
                const probD = invD / totalInv;

                // Probabilidades DC Sumadas
                const prob1X = probH + probD;
                const prob12 = probH + probA;
                const probX2 = probD + probA;

                // Cuotas Fair (Sin Vig) para DC
                doubleChance = {
                    homeDraw: Number((1 / prob1X).toFixed(3)),     // 1X
                    homeAway: Number((1 / prob12).toFixed(3)),     // 12
                    drawAway: Number((1 / probX2).toFixed(3))      // X2
                };
            }
        }

        // ----------------------------------------------------------------
        // 2. TOTALS (Over/Under)
        // ----------------------------------------------------------------
        // Buscamos todos los totales abiertos full match
        const totalMarkets = data.filter(m => m.type === 'total' && m.period === 0 && m.status === 'open');
        let totals = [];
        
        if (totalMarkets.length > 0) {
            totals = totalMarkets.map(m => {
                const overP = m.prices.find(p => p.designation === 'over');
                const underP = m.prices.find(p => p.designation === 'under');
                
                // Algunos formats usan m.points, otros lo tienen en price
                const line = m.points !== undefined ? m.points : (overP?.points);

                if (line !== undefined && overP && underP) {
                    return {
                        line: Number(line),
                        over: Number(americanToDecimal(overP.price).toFixed(3)),
                        under: Number(americanToDecimal(underP.price).toFixed(3))
                    };
                }
                return null;
            }).filter(Boolean);
            
            // Ordenar por diff absoluta con linea "estándar" 2.5 para priorizar
            // o simplemente devolver todos
            totals.sort((a,b) => a.line - b.line);
        }

        return {
            moneyline,
            doubleChance,
            totals,
            timestamp: Date.now()
        };

    } catch (error) {
        // Silenciar errores 404/403 para no ensuciar logs excesivamente
        // console.error(`❌ Error fetching Pinnacle Live Odds (${pinnacleMatchId}):`, error.message);
        return null;
    }
};

/**
 * Calcula la Probabilidad Implícita Real (Fair Chance) removiendo el Vig (Margen).
 * Usa método multiplicativo simple o Power method si se requiere más precisión.
 * @param {number} odd 
 * @param {number} totalImpliedProb - Suma de 1/odd de todas las opciones del mercado
 * @returns {number} Probabilidad en % (0-100)
 */
export const calculateNoVigProb = (odd, totalImpliedProb) => {
    if (!odd || odd <= 1) return 0;
    // Implied Prob raw
    const rawP = 1 / odd;
    // Fair Prob = Raw / Total (Normalización)
    const fairP = rawP / totalImpliedProb;
    return fairP * 100;
};
