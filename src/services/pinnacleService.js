                const awayPart = ev.participants?.find(p => p.alignment === 'away') || { name: ev.away || "Unknown Away" };

                if (isExcludedMarketVariant({
                    home: homePart.name,
                    away: awayPart.name,
                    league: ev.league?.name,
                    units: ev.units
                })) {
                    continue;
                }

                // Build Prices (Soporte Dual: Estructura Plana vs Anidada Legacy)
                let moneyline = null;
                let totals = [];
                
