const DEFAULT_PREMATCH_PRIMARY_HOURS = 6;
const DEFAULT_PREMATCH_PREFETCH_HOURS = 6;
const DEFAULT_PREMATCH_OVERLAP_MINUTES = 30;
const EXCLUDED_MATCH_TERMS = [
    'corners',
    'corner',
    'bookings',
    'booking',
    'cards',
    'card',
    'tarjetas',
    '8 games',
    '8 game'
];

const normalizeMarketText = (value = '') => String(value)
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase();

const isExcludedMarketVariant = ({ home = '', away = '', league = '', units = '' } = {}) => {
    if (units && String(units).toLowerCase() !== 'regular') return true;
    const blob = normalizeMarketText(`${home} ${away} ${league} ${units}`);
    return EXCLUDED_MATCH_TERMS.some(term => blob.includes(term));
};

const getPrematchWindowUtc = () => {
    const parsePositive = (value, fallback) => {
        const parsed = Number(value);
        return Number.isFinite(parsed) && parsed > 0 ? parsed : fallback;
    };

    const nowUtcMs = Date.now();
    const primaryHours = parsePositive(process.env.PREMATCH_WINDOW_PRIMARY_HOURS, DEFAULT_PREMATCH_PRIMARY_HOURS);
    const prefetchHours = parsePositive(process.env.PREMATCH_WINDOW_PREFETCH_HOURS, DEFAULT_PREMATCH_PREFETCH_HOURS);
    const overlapMinutes = parsePositive(process.env.PREMATCH_WINDOW_OVERLAP_MINUTES, DEFAULT_PREMATCH_OVERLAP_MINUTES);
    const totalHours = primaryHours + prefetchHours;

    const startUtcMs = nowUtcMs - (overlapMinutes * 60 * 1000);
    const endUtcMs = nowUtcMs + (totalHours * 60 * 60 * 1000);
    return { nowUtcMs, startUtcMs, endUtcMs, primaryHours, prefetchHours, overlapMinutes };
};

// Conversor de American Odds a Decimal (Pinnacle usa American)
const americanToDecimal = (american) => {
    if (!american) return 0;
    if (american > 0) {
        return (american / 100) + 1;
    } else {
        return (100 / Math.abs(american)) + 1;
    }
};

const parseScoreValue = (value) => {
    if (value === null || value === undefined || value === '') return null;
    const n = Number(value);
    if (!Number.isFinite(n)) return null;
    if (n < 0) return null;
    return Math.floor(n);
};

// HELPER: Calcular Double Chance desde 1x2 (para no depender de la API)
const calculateDCFromMoneyline = (homeStr, drawStr, awayStr) => {
    // Input puede ser string o number
    const h = Number(homeStr);
    const d = Number(drawStr);
    const a = Number(awayStr);
    if (!h || !d || !a) return null;

    // Inversas (Probabilidad Implícita Bruta)
    const probH = 1/h;
    const probD = 1/d;
    const probA = 1/a;
    
    // Sumas para DC
    const dc1X = 1 / (probH + probD);
    const dc12 = 1 / (probH + probA);
    const dcX2 = 1 / (probD + probA);

    return {
        homeDraw: Number(dc1X.toFixed(3)),
        homeAway: Number(dc12.toFixed(3)),
        drawAway: Number(dcX2.toFixed(3))
    };
};

